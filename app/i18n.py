def get_locale() -> str:
    loc = getattr(g, "locale", None)
    if loc in SUPPORTED_LOCALES:
        return loc
    return DEFAULT_LOCALE


def _(key: str, **kwargs) -> str:
    lang = get_locale()
    table = TRANSLATIONS.get(lang) or TRANSLATIONS[DEFAULT_LOCALE]
    s = table.get(key) or TRANSLATIONS[DEFAULT_LOCALE].get(key) or key
    return s.format(brand=BRAND_NAME, **kwargs)


def attach_locale_to_g() -> None:
    from flask import g

    q = (request.args.get("lang") or "").strip().lower()
    if q in SUPPORTED_LOCALES:
        g.locale = q
        return
    c = (request.cookies.get("locale") or "").strip().lower()
    if c in SUPPORTED_LOCALES:
        g.locale = c
        return
    g.locale = DEFAULT_LOCALE


def inject_template_globals() -> dict:
    lc = get_locale()
    return {
        "_": _,
        "brand": BRAND_NAME,
        "lang_choices": LANGUAGE_CHOICES,
        "current_locale": lc,
        "html_lang": HTML_LANG.get(lc, "en"),
    }
